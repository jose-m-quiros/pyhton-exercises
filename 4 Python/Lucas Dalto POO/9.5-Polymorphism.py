# Tipo real:

#tipo de dato actual de un objeto o variable en un momento específico durante la ejecución del programa.

# Definir una variable inicialmente como un entero
x = 10

# Verificar el tipo real de la variable
print(type(x))  # Salida: <class 'int'>

# Asignar un nuevo valor de tipo cadena a la misma variable
x = "Hola"

# Verificar el nuevo tipo real de la variable
print(type(x))  # Salida: <class 'str'>



#Este cambio de tipo real es posible en Python debido al tipado dinámico, 
# que permite que una variable cambie su tipo en tiempo de ejecución según 
# el valor que tenga asignado en un momento específico. 