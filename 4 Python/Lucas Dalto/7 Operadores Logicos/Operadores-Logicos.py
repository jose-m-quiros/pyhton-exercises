#AND & | Para que AND & nos devuelva true el valor A y el valor B ambos tienen que ser verdaderos | Con que uno sea false ya nos devulve todo false
Resultado = True & True #Devuelve True
print(1)
print(Resultado)
print("------------")


Resultado2 = False & True #Devuelve False
print(2)
print(Resultado2)
print("------------")


Resultado3 = True & False #Devuelve False
print(3)
print(Resultado3)
print("------------")


Resultado4 = False & False #Devuelve False
print(4)
print(Resultado4)
print("------------")


#OR | El operador OR nos va a devolver false solamente si las 2 condiciones con falsas
Resultado5 = True | True #Devuelve True
print(5)
print(Resultado5)
print("------------")


Resultado6 = False | True #Devuelve True
print(6)
print(Resultado6)
print("------------")


Resultado7 = True | False #Devuelve True
print(7)
print(Resultado7)
print("------------")


Resultado8 = False | False #Devuelve False
print(8)
print(Resultado8)
print("------------")


#NOT | Lo que hace NOT es invertir el valor | Si es true lo convierte a false | Si es false lo convierte a true
Resultado9 = not True #Devuelve Falso
print(9)
print(Resultado9)
print("------------")


Resultado10 = not False #Devuelve True
print(10)
print(Resultado10)
