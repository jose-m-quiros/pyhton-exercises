# == Es igual que

# != Es distinto que

# < Es menor que

# <= Es menor o igual que 

# > Es mayor que 

# >= Es mayor o igual que 


igual_que = 5 == 6
print(igual_que)


distinto_de = 5 != 6
print(distinto_de)


menor_que = 5 < 6
print(menor_que)


menor_o_igual_que = 5 <= 6
print(menor_o_igual_que)


mayor_que = 5 > 6
print(mayor_que)


mayor_o_igual_que = 5 >= 6
print(mayor_o_igual_que)

