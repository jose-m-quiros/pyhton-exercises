#El while se va a ejecutar siempre que una condicion sea verdadera

#Contador que se va a ir sumando
Contador = 0 

#Mientras que la condicion se cumpla, el bucle se va a seguir ejecutando 
#(Vuelta, tras vuelta se verifica la condicion)
while Contador < 10:
    Contador += 1
    print(Contador)
    
print("El while termino ")